<h3>Vision</h3>
<p>The Centre for Legal Aid Services (CLAS) aims to provide students with a new perspective to understand the actual realities of the law. The Center works to impart practical exposure to law exclusively for law students of Geeta Institute of Law, Panipat, Delhi-NCR. The Centres’ aim is to legally empower the marginalized and excluded groups of the society by providing effective legal representation, legal literacy, and awareness and bridging the gap between the legally available benefits and the entitled beneficiaries.​<br><br>
The Centre works to strengthen the system of Lok Adalats and other Alternate Dispute Resolution mechanisms in order to provide for informal, quick, inexpensive, and effective resolution of disputes and minimize a load of adjudication on the overburdened judiciary. </p><br>
